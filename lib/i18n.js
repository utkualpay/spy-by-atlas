// Get translation — client-side. Returns null if key not found (so caller can fallback).
export function t(key, lang = "en") {
  const dict = TRANSLATIONS[lang] || TRANSLATIONS.en;
  return dict[key] || TRANSLATIONS.en[key] || null;
}

// Get current language from localStorage (client-side only)
export function getLang() {
  if (typeof window === "undefined") return "en";
  return localStorage.getItem("spy-lang") || "en";
}

export function setLang(code) {
  if (typeof window !== "undefined") {
    localStorage.setItem("spy-lang", code);
    window.dispatchEvent(new Event("langchange"));
  }
}
